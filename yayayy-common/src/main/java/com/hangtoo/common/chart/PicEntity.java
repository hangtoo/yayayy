package com.hangtoo.common.chart;

import java.awt.image.BufferedImage;

public class PicEntity {
	BufferedImage pic;
	int x;
	int y;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public BufferedImage getPic() {
		return pic;
	}
	public void setPic(BufferedImage pic) {
		this.pic = pic;
	}
	
}
