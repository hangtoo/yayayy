package com.hangtoo.common.chart;

import java.util.List;

public class StringLine {
	
	List<StringElement> list;
	int x;
	int y;
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public List<StringElement> getList() {
		return list;
	}
	public void setList(List<StringElement> list) {
		this.list = list;
	}
	
}
